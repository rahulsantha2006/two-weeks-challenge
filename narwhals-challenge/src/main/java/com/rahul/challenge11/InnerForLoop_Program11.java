package com.rahul.challenge11;

public class InnerForLoop_Program11 {

    public static void main(String[] args) {


        for (int a=1; a<=5; a++){
            System.out.println("Regular For Loop");

            for (int b=a; b<=4; b++){
                System.out.println("Inner For Loop");
            }
        }



    }
}
